let flag = 0;

function getans() {
  if (flag == 0) {
    let answer = document.getElementById("faq-ans");
    answer.style.display = "block";
    answer.style.transition = "all 2s";
    let butn = document.getElementById("btn");
    butn.style.rotate = "90deg";
    flag = 1;
  } else {
    let answer = document.getElementById("faq-ans");
    answer.style.display = "none";
    answer.style.transition = "all 2s";
    let butn = document.getElementById("btn");
    butn.style.rotate = "44deg";
    flag = 0;
  }
}

